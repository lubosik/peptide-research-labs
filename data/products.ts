/**
 * Get product by slug
 */
export function getProductBySlug(slug: string): Product | undefined {
  return products.find((product) => product.slug === slug);
}

/**
 * Get products by category
 */
export function getProductsByCategory(category: string): Product[] {
  return products.filter((product) => product.category === category);
}

/**
 * Get the minimum price from a product's variants (or legacy price)
 * Used for displaying "from $XX.XX" in product grids
 */
export function getProductMinPrice(product: Product): number {
  if (product.variants && product.variants.length > 0) {
    return Math.min(...product.variants.map(v => v.price));
  }
  return product.price ?? 0;
}

/**
 * Get the maximum price from a product's variants (or legacy price)
 */
export function getProductMaxPrice(product: Product): number {
  if (product.variants && product.variants.length > 0) {
    return Math.max(...product.variants.map(v => v.price));
  }
  return product.price ?? 0;
}

/**
 * Check if product has multiple variants
 */
export function hasVariants(product: Product): boolean {
  return !!(product.variants && product.variants.length > 1);
}

/**
 * Get variant by strength string
 */
export function getVariantByStrength(product: Product, strength: string): ProductVariant | undefined {
  if (!product.variants || product.variants.length === 0) {
    return undefined;
  }
  return product.variants.find(v => v.strength === strength);
}

/**
 * Get default variant (first variant, or legacy product data)
 */
export function getDefaultVariant(product: Product): ProductVariant | null {
  if (product.variants && product.variants.length > 0) {
    return product.variants[0];
  }
  // Legacy product without variants - create a synthetic variant
  if (product.price !== undefined) {
    return {
      strength: product.specification || 'standard',
      quantityPerOrder: 1,
      price: product.price,
      sku: product.sku || product.id,
      inStock: product.inStock ?? true,
      specification: product.specification,
    };
  }
  return null;
}

/**
 * Get product SKU (from first variant or legacy field)
 */
export function getProductSku(product: Product): string | undefined {
  if (product.variants && product.variants.length > 0) {
    return product.variants[0].sku;
  }
  return product.sku;
}

/**
 * Check if product is in stock (any variant in stock, or legacy field)
 */
export function isProductInStock(product: Product): boolean {
  if (product.variants && product.variants.length > 0) {
    return product.variants.some(v => v.inStock);
  }
  return product.inStock ?? true; // Default to true if not specified
}

