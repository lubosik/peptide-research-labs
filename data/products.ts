/**
 * Product Data - Imported from Peptides Site Full Price List PDF
 * 
 * Products are organized by category as specified in the price list.
 * Each product includes warehouse options with default multipliers.
 */

export interface WarehouseOption {
  priceMultiplier: number;
  description: string;
  available: boolean;
}

/**
 * Product Variant - Represents a specific strength/size option for a product
 * Each variant corresponds to one vial per order (quantityPerOrder: 1)
 */
export interface ProductVariant {
  strength: string; // e.g. "2mg", "5mg", "10mg", "100mg", "5000iu"
  quantityPerOrder: number; // Always 1 for single vial sales
  price: number; // Base price (overseas warehouse)
  sku: string;
  inStock: boolean;
  specification?: string; // Optional variant-specific specification
}

/**
 * Product - Unified product model with variant support
 * Products with multiple strengths/sizes are represented as a single entry with variants array
 */
export interface Product {
  id: string;
  slug: string;
  name: string; // Base name without strength (e.g. "Retatrutide" not "Retatrutide (5mg)")
  description: string;
  shortDescription: string;
  image: string;
  category: string;
  
  // Variants array - products with multiple strengths/sizes
  // Optional during migration - products without variants will use legacy price/sku/inStock fields
  variants?: ProductVariant[];
  
  // Legacy fields for backward compatibility (products without variants)
  // These are optional and will be deprecated in favor of variants
  price?: number; // Base price (overseas warehouse) - use variants[0].price if variants exist
  sku?: string; // Legacy SKU - use variants[0].sku if variants exist
  inStock?: boolean; // Legacy stock status - use variants[0].inStock if variants exist
  specification?: string; // Legacy specification - use variants[0].specification if variants exist
  
  // Warehouse Options (applies to all variants)
  warehouseOptions?: {
    overseas: WarehouseOption;
    us: WarehouseOption;
  };
  
  // Chemical Information (shared across all variants)
  chemicalFormula?: string;
  molarMass?: string;
  casNumber?: string;
  synonyms?: string[];
  pubchemId?: string;
  shelfLife?: string;
  
  // Research Applications (shared across all variants)
  researchApplications?: string;
  
  // Safety & Handling (shared across all variants)
  storageRequirements?: string;
  handlingGuidelines?: string;
}

